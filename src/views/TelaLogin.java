    public boolean checkFields() {
        if(txtNome.getText().isEmpty() || txtEmail.getText().isEmpty() || txtTel.getText().isEmpty() || 
                txtNovaSenha.getText().isEmpty()) {
            lbErroCad.setText("Preencha todos os campos para realizar o cadastro");
            return false;
        } else if(!(txtNovaSenha.getText().equals(txtRepSenha.getText()))) {
            lbErroCad.setText("As senhas digitadas não são iguais");
            return false;
        } else {
            return true;
        }          
    }

