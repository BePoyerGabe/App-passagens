    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (checkFields()) {
            Cliente cliente = new Cliente();
            cliente.setNome(txtNome.getText());
            cliente.setEmail(txtEmail.getText());
            cliente.setTelefone(txtTel.getText());
            cliente.setSenha(txtNovaSenha.getText());
            
            ClienteDAO dao = new ClienteDAO();
            dao.newCliente(cliente);
            
            dispose();
            TelaPrincipal tp = new TelaPrincipal(cliente);
            tp.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        if (txtUser.getText().isEmpty() || txtSenha.getText().isEmpty())  {
            lbErro.setText("Preencha todos os campos para realizar o login");
        } else {
            ClienteDAO dao = new ClienteDAO();
            Cliente res = dao.login(txtUser.getText(), txtSenha.getText());
            System.out.println(res.getNome() + " " + res.getSenha());
            
            if(res.getNome() == null) {
                lbErro.setText("Usuário não cadastrado!!");
                
            } else {
                
                dispose();
                TelaPrincipal telaPrincipal = new TelaPrincipal(res);
                telaPrincipal.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnEntrarActionPerformed

